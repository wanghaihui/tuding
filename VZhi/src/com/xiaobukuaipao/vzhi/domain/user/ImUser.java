package com.xiaobukuaipao.vzhi.domain.user;

public class ImUser {
	private long uid;
	private long did;
	private long gid;
	
	public ImUser() {
		
	}
	
	public void setUid(long uid) {
		this.uid = uid;
	}
	public long getUid() {
		return this.uid;
	}
	
	public void setDid(long did) {
		this.did = did;
	}
	public long getDid() {
		return this.did;
	}
	
	public void setGid(long gid) {
		this.gid = gid;
	}
	public long getGid() {
		return this.gid;
	}
}
